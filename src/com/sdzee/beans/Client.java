package com.sdzee.beans;

public class Client {
	private String cliNom;
	private String cliPrenom;
	private String cliAdresseLivraison;
	private String cliNumTel;
	private String cliEmail;
	
	public String getCliNom() {
		return cliNom;
	}
	
	public void setCliNom(String cliNom) {
		this.cliNom = cliNom;
	}
	
	public String getCliPrenom() {
		return cliPrenom;
	}
	
	public void setCliPrenom(String cliPrenom) {
		this.cliPrenom = cliPrenom;
	}
	
	public String getCliAdresseLivraison() {
		return cliAdresseLivraison;
	}
	
	public void setCliAdresseLivraison(String cliAdresseLivraison) {
		this.cliAdresseLivraison = cliAdresseLivraison;
	}
	
	public String getCliNumTel() {
		return cliNumTel;
	}
	
	public void setCliNumTel(String cliNumTel) {
		this.cliNumTel = cliNumTel;
	}
	
	public String getCliEmail() {
		return cliEmail;
	}
	
	public void setCliEmail(String cliEmail) {
		this.cliEmail = cliEmail;
	}
}
